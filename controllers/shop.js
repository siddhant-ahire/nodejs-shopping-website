exports.getInvoice = (req, res, next) => {
  const orderId = req.params.orderId;

  Order.findById(orderId)
    .then(order => {
      if (!order) {
        return next(new Error('Order not found'));
      }
      if (order.user.userId.toString() !== req.user._id.toString()) {
        return next(new Error('unauthorise'));
      }
      const invoiceName = `${orderId}-0.7301055647549854-Wed Sep 02 2020-Screenshot_20200831-181933.png`
      const invoicePath = path.join('data', 'invoice', invoiceName);
      const pdfDoc = new PDFDocument();
      pdfDoc.pipe(fs.createWriteStream(invoicePath));
      pdfDoc.pipe(res);
      let previousProductsPrice = 0;
      order.products.forEach(prod => {
        previousProductsPrice += prod.product.price;
        pdfDoc.fontSize(30).text(`Title:${prod.product.title}`, { underline: true });
        pdfDoc.fontSize(14).text(`Price:${prod.product.price}`);
        pdfDoc.text('------------------------------');


      });
      pdfDoc.fontSize(30).text(`Total Price: ${previousProductsPrice}`)
      pdfDoc.end();
    })
    .catch(err =>{
      const error = new Error(err);
      error.httpStatusCode = 500;
      return next(error);
    })
}